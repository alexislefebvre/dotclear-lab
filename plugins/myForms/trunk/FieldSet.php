class MyFormsFieldSet
{
  private $fields;
  
  public function __construct() {
    $this->fields = array();
  }
  
  public function add($field) {
    $this->fields[$field->Name()]=$field;
  }
  
  public function get($name) {
    return $this->fields[$name];
  }
    
}
